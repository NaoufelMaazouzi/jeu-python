class World():
    def __init__(self, window):
        self.window = window
        self.screen_width = window.get_width()
        self.screen_height = window.get_height()
        self.tile_list = []
        ground_img = pygame.image.load(os.path.join('images/Tile', "2.png")).convert_alpha()
        sand_img = pygame.image.load(os.path.join('images/Tile', "5.png")).convert_alpha()
        door_img = pygame.image.load(os.path.join('images', "door.png")).convert_alpha()
        row_count = 0
        for row in world_data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(ground_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect, 'test')
                    self.tile_list.append(tile)
                if tile == 2:
                    img = pygame.transform.scale(sand_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect, '')
                    self.tile_list.append(tile)
                if tile == 3:
                    blob = Enemy(col_count * tile_size + 30, row_count * tile_size - 70, self.window)
                    globalVariables.blob_group.add(blob)
                if tile == 4:
                    img = pygame.transform.scale(door_img, (tile_size + 150, tile_size + 150))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect, 'exit')
                    self.tile_list.append(tile)
                col_count += 1
            row_count += 1
    
    def draw_grid(self):
        for line in range(0, 31):
            pygame.draw.line(self.window, (255, 255, 255), (0, line * tile_size), (self.screen_width, line * tile_size))
            pygame.draw.line(self.window, (255, 255, 255), (line * tile_size, 0), (line * tile_size, self.screen_height))
    
    def draw(self):
        for tile in self.tile_list:
            self.window.blit(tile[0], tile[1])

    def launch_meteorites(self):
        while True:
            for _ in range(10):
                x = randrange(self.screen_width)
                y = randrange(-500, 1)
                globalVariables.meteorites.add(Meteorite(x, y, self.window))
            time.sleep(5)